function cityCountry(city, country) {
    return "".concat(city, " , ").concat(country);
}
//calling a function & print the returned value
cityCountry("karachi", "pakistan");
console.log(cityCountry("karachi", "pakistan"));
cityCountry("tokyo", "japan");
console.log(cityCountry("tokya", "japan"));
cityCountry("Berlin", "germany");
console.log(cityCountry("berlin", "germany"));
